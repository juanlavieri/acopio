const DICTS = { en, es };

const I18nCtx = createContext(null);

function detectDefault() {
  const saved = localStorage.getItem(STORE_KEY);
  if (saved === "en" || saved === "es") return saved;
  return (navigator.language || "es").toLowerCase().startsWith("en") ? "en" : "es";
}

export function LanguageProvider({ children }) {
  const [lang, setLangState] = useState(detectDefault);

  const setLang = useCallback((l) => {
    setLangState(l);
    localStorage.setItem(STORE_KEY, l);
  }, []);

  const t = useCallback(
    (key, vars) => {
      let s = DICTS[lang]?.[key] ?? en[key] ?? key;
      if (vars) for (const k of Object.keys(vars)) s = s.replace(`{${k}}`, vars[k]);
      return s;
    },
    [lang]
  );

  const roleName = useCallback((role) => t(`role.${role}`), [t]);
  const kindName = useCallback((kind) => t(`kind.${kind || "other"}`), [t]);

  return (
    <I18nCtx.Provider value={{ lang, setLang, t, roleName, kindName }}>{children}</I18nCtx.Provider>
  );
}

export function useT() {
  return useContext(I18nCtx);
}
